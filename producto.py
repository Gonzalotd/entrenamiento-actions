"""Módulo que contiene la función para calcular el producto de dos números."""

def producto(a, b):
    """Calcula el producto de dos números.
    
    Args:
        a (int): Primer factor.
        b (int): Segundo factor.
    
    Returns:
        int: Resultado de multiplicar `a` por `b`.
    """

    return a * b


if __name__ == "__main__":
    print(producto(7, 8))
